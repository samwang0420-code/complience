// 为每个错误代码添加详细解决方案
function addDetailedSolutions(db) {
  const categories = {
    'Washer': 'washer',
    'HVAC': 'hvac',
    'PLC': 'plc',
    'CNC': 'plc',
    'Robotics': 'plc'
  }

  db.errorCodes.forEach((item, index) => {
    const catKey = categories[item.category] || 'common'
    const solutions = SOLUTION_TEMPLATES[catKey]
    
    if (solutions) {
      // 根据错误代码选择解决方案
      let selectedSolution = null
      
      const codeLower = item.code.toLowerCase()
      const nameLower = item.name.toLowerCase()
      
      // 匹配
      for (const [key, sol] of Object.entries(solutions)) {
        if (codeLower.includes('f21') || codeLower.includes('drain') || nameLower.includes('drain')) {
          selectedSolution = solutions.drain || sol
          break
        } else if (codeLower.includes('door') || codeLower.includes('de') || nameLower.includes('door')) {
          selectedSolution = solutions.door || sol
          break
        } else if (codeLower.includes('e1') || codeLower.includes('water') || nameLower.includes('inlet')) {
          selectedSolution = solutions.water || sol
          break
        } else if (codeLower.includes('motor') || codeLower.includes('f6') || nameLower.includes('motor')) {
          selectedSolution = solutions.motor || sol
          break
        } else if (codeLower.includes('pressure') || codeLower.includes('e1') || codeLower.includes('high')) {
          selectedSolution = solutions.pressure || sol
          break
        } else if (codeLower.includes('comm') || codeLower.includes('communication')) {
          selectedSolution = solutions.communication || sol
          break
        } else if (codeLower.includes('cpu') || codeLower.includes('sf')) {
          selectedSolution = solutions.cpu || sol
          break
        } else if (codeLower.includes('io') || codeLower.includes('fault')) {
          selectedSolution = solutions.io || sol
          break
        }
      }
      
      // 如果没有匹配，添加通用步骤
      if (selectedSolution) {
        item.detailedSolution = selectedSolution
      }
    }
  })
  
  return db
}

// 读取并更新数据库
const db = JSON.parse(fs.readFileSync(DB_PATH, 'utf8'))
const updatedDb = addDetailedSolutions(db)

// 保存
fs.writeFileSync(DB_PATH, JSON.stringify(updatedDb, null, 2))

console.log(`✅ 已为 ${updatedDb.errorCodes.length} 条错误代码添加详细解决方案`)

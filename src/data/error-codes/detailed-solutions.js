// 生成详细解决方案
function getDetailedSolution(brand, category, errorCode) {
  const categorySolutions = DETAILED_SOLUTIONS[category.toLowerCase()] || DETAILED_SOLUTIONS.common
  const errorType = getErrorType(errorCode)
  
  // 尝试匹配错误类型
  for (const [key, solution] of Object.entries(categorySolutions)) {
    if (errorCode.toLowerCase().includes(key.toLowerCase()) || 
        key.toLowerCase().includes(errorType)) {
      return solution
    }
  }
  
  // 返回通用解决方案
  return {
    steps: [
      "1. 断开设备电源",
      "2. 等待 5 分钟后重新上电",
      "3. 检查设备显示的错误代码",
      "4. 参考用户手册查找该代码含义",
      "5. 检查相关传感器和连接",
      "6. 清理设备内部灰尘",
      "7. 检查所有接线是否牢固",
      "8. 如果问题持续，联系专业维修"
    ],
    tools: ["螺丝刀", "万用表"],
    safety: "操作前断开电源",
    note: "这是通用步骤，具体请参考设备手册"
  }
}

function getErrorType(code) {
  const codePrefix = code.charAt(0).toUpperCase()
  const types = {
    'F': 'fault',
    'E': 'error',
    'L': 'lock',
    'P': 'pump',
    'C': 'calibration',
    'U': 'user',
    'S': 'system',
    'A': 'alarm'
  }
  return types[codePrefix] || 'fault'
}

